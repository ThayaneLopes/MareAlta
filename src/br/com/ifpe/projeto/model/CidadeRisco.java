package br.com.ifpe.projeto.model;

public class CidadeRisco {
	
	private int id;
	private String nome;
	private String regiao;
	private String situacao_risco;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getRegiao() {
		return regiao;
	}
	public void setRegiao(String regiao) {
		this.regiao = regiao;
	}
	public String getSituacao_risco() {
		return situacao_risco;
	}
	public void setSituacao_risco(String situacao_risco) {
		this.situacao_risco = situacao_risco;
	}
	
	

}
